function solution(number: string, k: number): string {
  const stack: string[] = [];
  let removeCount = 0;

  for (const num of number) {
    while (
      stack.length > 0 &&
      removeCount < k &&
      stack[stack.length - 1] < num
    ) {
      stack.pop();
      removeCount++;
    }
    stack.push(num);
  }

  while (removeCount < k) {
    stack.pop();
    removeCount++;
  }

  return stack.join("");
}

