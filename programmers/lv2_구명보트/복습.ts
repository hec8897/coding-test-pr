function solution(people: number[], limit: number): number {
  const peoples = people.sort((a, b) => a - b);
  let left = 0;
  let right = people.length - 1;
  let count = 0;

  while (left <= right) {
    if (peoples[left] + peoples[right] <= limit) {
      left++;
      right--;
    } else {
      right--;
    }
    count++;
  }
  return count; // TODO: λ³΄νΈ κ°μ λ°ν™
}

